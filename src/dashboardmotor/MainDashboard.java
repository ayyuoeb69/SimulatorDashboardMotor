    int Gear = 0, max, rpm;
    Timer gasPlus = new Timer(90, new ActionListener() {   
        @Override
        public void actionPerformed(ActionEvent e) {
            int up = Integer.parseInt(txfSpeed.getText());
            switch(Gear) {
                case 1: max = 20; break;
                case 2: max = 40; break;
                case 3: max = 80; break;
                case 4: max = 140; break;
            }
            
            if (up < max) {
                up++;
            }
            
            rpm = up / max * 1000;
            
            txfRpm.setText(String.valueOf(rpm));
            txfSpeed.setText(String.valueOf(up));
        }
    });
    
    Timer gasMinus = new Timer(600, new ActionListener() {       
        @Override
        public void actionPerformed(ActionEvent e) {
        int up = Integer.parseInt(txfSpeed.getText());    
            if (up > 0) {
                up--;
            }  
            
            rpm = up / max * 1000;
            
            txfRpm.setText(String.valueOf(rpm));
            txfSpeed.setText(String.valueOf(up));
        }
    });
    
    Timer rem = new Timer(150, new ActionListener() {       
        @Override
        public void actionPerformed(ActionEvent e) {
        int up = Integer.parseInt(txfSpeed.getText());    
            if (up > 0) {
                up--;
            }     
            
            rpm = up / max * 1000;
            
            txfRpm.setText(String.valueOf(rpm));
            txfSpeed.setText(String.valueOf(up));
        }
    });
    
