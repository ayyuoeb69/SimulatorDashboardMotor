    private void tglOnOffItemStateChanged(java.awt.event.ItemEvent evt) {//GEN-FIRST:event_tglOnOffItemStateChanged
        int state = evt.getStateChange();
        if (state == java.awt.event.ItemEvent.SELECTED) {
            tglOnOff.setText("Matikan");
            btnUp.setEnabled(true);
            btnDown.setEnabled(true);
            btnRem.setEnabled(true);
            btnGas.setEnabled(true);
        } else {
            tglOnOff.setText("Hidupkan");
            btnUp.setEnabled(false);
            btnDown.setEnabled(false);
            btnRem.setEnabled(false);
            btnGas.setEnabled(false);
        }
    }//GEN-LAST:event_tglOnOffItemStateChanged

