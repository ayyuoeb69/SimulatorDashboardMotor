    private void btnUpMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_btnUpMouseClicked
        Gear = Integer.parseInt(txfGear.getText());
        if (Gear < 4) {
            Gear++;
        }
        txfGear.setText(String.valueOf(Gear));
    }//GEN-LAST:event_btnUpMouseClicked

    private void btnDownMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_btnDownMouseClicked
        Gear = Integer.parseInt(txfGear.getText());
        if (Gear > 0) {
            Gear--;
        }
        txfGear.setText(String.valueOf(Gear));
    }//GEN-LAST:event_btnDownMouseClicked

    private void btnGasMousePressed(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_btnGasMousePressed
        
        btnGas.addMouseListener(new MouseAdapter() {
            @Override
            public void mousePressed(MouseEvent e) {           
                gasMinus.stop();
                gasPlus.start();
            }

            @Override
            public void mouseReleased(MouseEvent e) {
              gasPlus.stop();
              gasMinus.start();
            }
        });    
        
        
    }//GEN-LAST:event_btnGasMousePressed

